    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        int check = 0;
        String id = txt_id.getText();
        try {

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from member where id=' " + id + " ' ");
            while (rs.next()) {
                check = 1;
                txt_id.setEditable(false);
                txt_name.setText(rs.getString(2));
                txt_phone.setText(rs.getString(3));
                txt_email.setText(rs.getString(4));
                txt_Gender.setText(rs.getString(5));
                txt_Gender.setEditable(false);
                txt_time.setText(rs.getString(6));
                txt_time.setEditable(false);
                txt_age.setText(rs.getString(7));
                txt_amount.setText(rs.getString(8));

            }
            if (check == 0) {
                JOptionPane.showMessageDialog(null, "Member doesn't exist");
            }

        } catch (SQLException ex) {
            Logger.getLogger(UpdateOrDelete.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btn_searchActionPerformed

