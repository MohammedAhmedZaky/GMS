    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        String id = txt_id.getText();
        String name = txt_name.getText();
        String phone = txt_phone.getText();
        String email = txt_email.getText();
        String gender = txt_Gender.getText();
        String time = txt_time.getText();
        String age = txt_age.getText();
        String amount = txt_amount.getText();
        try {
            // TODO add your handling code here:

            PreparedStatement st = con.prepareStatement("update member set name=?,phonenum=?,email=?,age=?,amount=?where id=?");
            st.setString(1, name);
            st.setString(2, phone);
            st.setString(3, email);
            st.setString(4, age);
            st.setString(5, amount);
            st.setString(6, id);
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "successfully Update ");
            setVisible(false);
            new UpdateOrDelete().setVisible(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_btn_updateActionPerformed

